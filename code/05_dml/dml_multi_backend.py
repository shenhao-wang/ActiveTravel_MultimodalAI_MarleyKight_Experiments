def main():
    parser = argparse.ArgumentParser(description='Multi-backend DML comparison')
    parser.add_argument('--backends', type=str, default=None,
                        help='Comma-separated list of backends to run '
                             '(default: all available)')
    # 5-fold cross-fitting; hyperparameter fixed across all estimands to avoid separate tuning per nuisance
    parser.add_argument('--n-folds', type=int, default=5)
    parser.add_argument('--n-seeds', type=int, default=10,
                        help='Number of seeds for repeated CV (default: 10, '
                             'giving 10×5=50 fits)')
    parser.add_argument('--build-hybrid', action='store_true', default=True,
                        help='Build hybrid embeddings from LLM + MiniLM')
    args = parser.parse_args()

    print('=' * 65)
    print('  Tampa Active Travel — Multi-Backend DML Comparison')
    print('=' * 65)

    # ── Build hybrid if both sources exist
    llm_path = 'zone_embeddings_llm_scored.csv'
    st_path  = 'zone_embeddings_minilm.csv'
    hybrid_path = 'zone_embeddings_hybrid.csv'
    if (args.build_hybrid and os.path.exists(llm_path)
            and os.path.exists(st_path)
            and not os.path.exists(hybrid_path)):
        print('\nBuilding hybrid embeddings...')
        build_hybrid_embeddings(llm_path, st_path, hybrid_path)

    # ── Determine which backends to run
    if args.backends:
        requested = set(args.backends.split(','))
        backends = [(k, f, l) for k, f, l in ALL_BACKENDS if k in requested]
    else:
        backends = [(k, f, l) for k, f, l in ALL_BACKENDS if os.path.exists(f)]

    if not backends:
        print('\nERROR: No embedding files found. Run embedding_pipeline_v3.py '
              'and/or llm_zoning_features.py first.')
        return

    print(f'\nBackends available: {[b[0] for b in backends]}')

    # ── Load shared data
    print('\nLoading base data...')
    trips, zon_csv, bg_features, acs_cols, area_col = load_base_data()
    W_COLS = INDIVIDUAL_CONFOUNDERS + acs_cols
    Y_COL = 'active_travel'

    # ── Nuisance model specs (matching tampa_dml_v2.py)
    # Hyperparams set by pilot CV on outcome model, then frozen.
    # RF over lasso: nonlinear confounding favors trees.
    clf_Y = RandomForestClassifier(
        n_estimators=500, max_depth=12, min_samples_leaf=5,
        random_state=RANDOM_STATE, n_jobs=-1)
    reg_T = RandomForestRegressor(
        n_estimators=500, max_depth=12, min_samples_leaf=5,
        random_state=RANDOM_STATE, n_jobs=-1)

    # ── Run DML for each backend
    results = {}
    all_theta_rows = []
    all_c_rows = []

    for backend_key, emb_file, backend_label in backends:
        print(f'\n{"─" * 60}')
        print(f'  Backend: {backend_label} ({emb_file})')
        print(f'{"─" * 60}')

        emb_df = pd.read_csv(emb_file)
        emb_cols = [c for c in emb_df.columns
                    if c.startswith('emb_') or c.startswith('feat_')]
        n_dims = len(emb_cols)
        print(f'  Dimensions: {n_dims}')

        # Build BG-level embeddings
        bg_emb = build_bg_embeddings(zon_csv, emb_df, emb_cols, area_col)
        df = trips.merge(
            bg_features.merge(bg_emb, on='GEOID', how='left'),
            left_on='o_bg', right_on='GEOID', how='left')
        df = df.dropna(subset=['zoning_entropy']).reset_index(drop=True)

        avail_w = [c for c in W_COLS if c in df.columns]
        avail_emb = [c for c in emb_cols if c in df.columns]

        if len(avail_emb) == 0:
            print(f'  SKIPPED: no embedding columns found in merged data')
            continue

        Y = df[Y_COL].values.astype(int)
        W = df[avail_w].values.astype(float)
        T_mat = df[avail_emb].values.astype(float)

        # Handle NaN in T
        nan_mask = np.isnan(T_mat).any(axis=1) | np.isnan(W).any(axis=1)
        valid = ~nan_mask
        Y, W, T_mat = Y[valid], W[valid], T_mat[valid]
        print(f'  Sample: {len(Y):,} trips  |  AT rate: {Y.mean():.1%}')

        # Repeated cross-fitted DML (10 seeds × 5 folds = 50 fits)
        print(f'  Repeated DML ({args.n_seeds} seeds × {args.n_folds} folds '
              f'= {args.n_seeds * args.n_folds} fits)...')
        b_result = estimand_b_repeated(
            Y, T_mat, W, clf_Y, reg_T,
            n_folds=args.n_folds, n_seeds=args.n_seeds,
            base_seed=RANDOM_STATE, alpha=ALPHA)

        print(f'  Significant dims (BH α={ALPHA}): '
              f'{b_result["n_sig"]}/{n_dims}')
        print(f'  SE decomposition: within={b_result["se_within"].mean():.5f}, '
              f'across={b_result["se_across"].mean():.5f}')

        # Estimand C
        c_result = estimand_c(b_result['theta'], emb_df, emb_cols[:len(b_result['theta'])])
        top8 = c_result.head(8)['zone_class'].tolist()
        bot8 = c_result.tail(8)['zone_class'].tolist()
        print(f'  Top 8 AT-promoting: {", ".join(top8)}')
        print(f'  Bottom 8 (car-promoting): {", ".join(bot8)}')

        # Store results
        results[backend_key] = {
            'label': backend_label,
            'n_dims': n_dims,
            'n_sig': b_result['n_sig'],
            'theta': b_result['theta'],
            'se': b_result['se'],
            'pval': b_result['pval'],
            'bh_sig': b_result['bh_sig'],
            'estimand_c': c_result,
        }

        # Theta rows for comparison CSV
        for j in range(n_dims):
            col_name = avail_emb[j] if j < len(avail_emb) else f'dim_{j:03d}'
            all_theta_rows.append({
                'backend': backend_key,
                'dimension': col_name,
                'theta': b_result['theta'][j],
                'se': b_result['se'][j],
                'se_within': b_result['se_within'][j],
                'se_across': b_result['se_across'][j],
                'pval': b_result['pval'][j],
                'p_bh': b_result['p_bh'][j],
                'bh_sig': b_result['bh_sig'][j],
                'n_fits': b_result['n_fits'],
            })

        # Estimand C rows
        for _, row in c_result.iterrows():
            all_c_rows.append({
                'backend': backend_key,
                'zone_class': row['zone_class'],
                'context': row['context'],
                'causal_score': row['causal_score'],
            })

    # ── Save comparison outputs
    if not results:
        print('\nNo backends ran successfully.')
        return

    theta_df = pd.DataFrame(all_theta_rows)
    theta_df.to_csv('dml_backend_comparison.csv', index=False)
    print(f'\nSaved: dml_backend_comparison.csv')

    c_df = pd.DataFrame(all_c_rows)
    c_df.to_csv('dml_estimand_c_comparison.csv', index=False)
    print(f'Saved: dml_estimand_c_comparison.csv')

    # Summary table
    summary_rows = []
    for bk, res in results.items():
        c = res['estimand_c']
        summary_rows.append({
            'backend': bk,
            'label': res['label'],
            'n_dims': res['n_dims'],
            'n_significant': res['n_sig'],
            'pct_significant': res['n_sig'] / res['n_dims'] * 100,
            'top_3_at': ', '.join(c.head(3)['zone_class'].tolist()),
            'bottom_3_car': ', '.join(c.tail(3)['zone_class'].tolist()),
            'max_theta': res['theta'].max(),
            'min_theta': res['theta'].min(),
            'mean_abs_theta': np.abs(res['theta']).mean(),
        })
    summary_df = pd.DataFrame(summary_rows)
    summary_df.to_csv('dml_backend_summary.csv', index=False)
    print(f'Saved: dml_backend_summary.csv')

    # ── Summary figure
    fig, axes = plt.subplots(1, 3, figsize=(18, 6))
    fig.suptitle('DML Estimand B: Multi-Backend Comparison',
                 fontweight='bold', fontsize=13)

    bk_names = [res['label'] for res in results.values()]
    bk_keys = list(results.keys())
    colors = ['#4878CF', '#6ACC65', '#D65F5F', '#E89C3A',
              '#B47CC7', '#17BECF', '#8C564B']

    # Panel 1: Number of significant dimensions
    ax = axes[0]
    n_sigs = [results[k]['n_sig'] for k in bk_keys]
    n_dims_list = [results[k]['n_dims'] for k in bk_keys]
    x = np.arange(len(bk_keys))
    bars = ax.bar(x, n_sigs, color=colors[:len(bk_keys)], alpha=0.85)
    for i, (ns, nd) in enumerate(zip(n_sigs, n_dims_list)):
        ax.text(i, ns + 0.3, f'{ns}/{nd}', ha='center', va='bottom', fontsize=9)
    ax.set_xticks(x)
    ax.set_xticklabels(bk_names, rotation=25, ha='right', fontsize=8)
    ax.set_ylabel('Significant Dimensions (BH-corrected)')
    ax.set_title('Causal Signal Density')
    ax.grid(axis='y', alpha=0.3)

    # Panel 2: Mean |θ|
    ax = axes[1]
    mean_abs = [np.abs(results[k]['theta']).mean() for k in bk_keys]
    bars = ax.bar(x, mean_abs, color=colors[:len(bk_keys)], alpha=0.85)
    for i, v in enumerate(mean_abs):
        ax.text(i, v + 0.0002, f'{v:.4f}', ha='center', va='bottom', fontsize=9)
    ax.set_xticks(x)
    ax.set_xticklabels(bk_names, rotation=25, ha='right', fontsize=8)
    ax.set_ylabel('Mean |θ| (effect magnitude)')
    ax.set_title('Average Effect Magnitude')
    ax.grid(axis='y', alpha=0.3)

    # Panel 3: Estimand C rank correlation
    ax = axes[2]
    if len(results) > 1:
        # Pairwise Spearman rank correlation of zone causal scores
        ref_key = bk_keys[0]
        ref_c = results[ref_key]['estimand_c'].set_index('zone_class')['causal_score']
        corrs = []
        for k in bk_keys:
            other_c = results[k]['estimand_c'].set_index('zone_class')['causal_score']
            common = ref_c.index.intersection(other_c.index)
            if len(common) > 5:
                r, _ = stats.spearmanr(ref_c[common], other_c[common])
                corrs.append(r)
            else:
                corrs.append(np.nan)
        bars = ax.bar(x, corrs, color=colors[:len(bk_keys)], alpha=0.85)
        for i, v in enumerate(corrs):
            if not np.isnan(v):
                ax.text(i, v + 0.01, f'{v:.2f}', ha='center', va='bottom', fontsize=9)
        ax.set_ylim(-0.5, 1.1)
        ax.axhline(1.0, color='gray', linestyle='--', alpha=0.5)
        ax.set_ylabel(f'Spearman ρ vs {results[ref_key]["label"]}')
        ax.set_title('Zone Ranking Consistency')
    else:
        ax.text(0.5, 0.5, 'Need ≥2 backends\nfor comparison',
                ha='center', va='center', transform=ax.transAxes)
        ax.set_title('Zone Ranking Consistency')

    ax.set_xticks(x)
    ax.set_xticklabels(bk_names, rotation=25, ha='right', fontsize=8)
    ax.grid(axis='y', alpha=0.3)

    plt.tight_layout()
    plt.savefig('fig_dml_backend_comparison.png', dpi=150, bbox_inches='tight')
    plt.close()
    print(f'Saved: fig_dml_backend_comparison.png')

    # ── Print summary
    print(f'\n' + '=' * 70)
    print(f'  SUMMARY  ({args.n_seeds} seeds × {args.n_folds} folds '
          f'= {args.n_seeds * args.n_folds} fits per backend)')
    print('=' * 70)
    print(f'  {"Backend":<20} {"Dims":>5} {"Sig":>4} {"% Sig":>6} '
          f'{"Mean |θ|":>9} {"Top 3 AT":>30}')
    print('  ' + '─' * 76)
    for _, row in summary_df.iterrows():
        print(f'  {row["label"]:<20} {row["n_dims"]:>5} {row["n_significant"]:>4} '
              f'{row["pct_significant"]:>5.1f}% {row["mean_abs_theta"]:>9.5f} '
              f'{row["top_3_at"]:>30}')

    print(f'\n✓ Multi-backend DML comparison complete.')


