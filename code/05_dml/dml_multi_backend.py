# ─────────────────────────────────────────────────────────────
# DML CORE (simplified from tampa_dml_v2.py)
# ─────────────────────────────────────────────────────────────

def impute_scale(X_tr, X_te):
    """Per-fold impute/scale to avoid information leakage; global standardization
    before split would let test-set stats influence training."""
    imp = SimpleImputer(strategy='median')
    sc  = StandardScaler()
    X_tr_out = sc.fit_transform(imp.fit_transform(X_tr))
    X_te_out = sc.transform(imp.transform(X_te))
    return X_tr_out, X_te_out


def cross_fit_vector_residuals(Y, T_mat, W, clf_Y, reg_T,
                                n_folds=5, seed=16):
    """
    Cross-fitted DML residuals for VECTOR treatment T_mat (n × d).
    Returns Y_res (n,) and T_res (n × d).
    """
    n, d = T_mat.shape
    Y_res = np.zeros(n)
    T_res = np.zeros((n, d))

    # Stratify on outcome: AT is ~10% of trips, unstratified folds could be starved
    kf = StratifiedKFold(n_splits=n_folds, shuffle=True, random_state=seed)
    for fold, (tr_idx, te_idx) in enumerate(kf.split(W, Y)):
        W_tr, W_te = impute_scale(W[tr_idx], W[te_idx])

        # Outcome nuisance
        clf_f = clone(clf_Y)
        clf_f.fit(W_tr, Y[tr_idx])
        Y_hat = clf_f.predict_proba(W_te)[:, 1]
        Y_res[te_idx] = Y[te_idx] - Y_hat

        # Treatment nuisance: one regression per dimension
        imp_T = SimpleImputer(strategy='median')
        sc_T  = StandardScaler()
        T_tr = sc_T.fit_transform(imp_T.fit_transform(T_mat[tr_idx]))
        T_te = sc_T.transform(imp_T.transform(T_mat[te_idx]))

        for j in range(d):
            reg_f = clone(reg_T)
            reg_f.fit(W_tr, T_tr[:, j])
            T_hat = reg_f.predict(W_te)
            T_res[te_idx, j] = T_te[:, j] - T_hat

    return Y_res, T_res


def estimand_b_repeated(Y, T_mat, W, clf_Y, reg_T,
                        n_folds=5, n_seeds=10, base_seed=16,  # 10 seeds x 5 folds = 50 fits to reduce sensitivity to any single train/test partition
                        alpha=0.05):
    """
    Repeated cross-fitted DML for vector treatment.

    Design: n_seeds × n_folds = 50 fits total.
    Each seed produces a different fold assignment. The final θ is
    the median across all seed-level θ estimates (median is more
    robust to outlier seeds than the mean). SEs are sandwich
    (HC1) per seed, then combined via the formula:
        SE_combined = median(SE_within) + SD(θ_across_seeds)
    which accounts for both within-fold and across-seed variance.

    Returns dict with theta, se, pval, bh_sig, per_seed_thetas.
    """
    d = T_mat.shape[1]
    n = len(Y)

    seed_thetas = []
    seed_ses = []

    for s in range(n_seeds):
        seed = base_seed + s

        # Cross-fit with this seed's fold assignment
        Y_res, T_res = cross_fit_vector_residuals(
            Y, T_mat, W, clf_Y, reg_T,
            n_folds=n_folds, seed=seed)

        # OLS on residuals, not logistic -- second stage operates on continuous
        # (Y - g_hat(W)), not binary outcome directly.
        # No intercept: DML residuals are mean-zero by construction; intercept would absorb nothing.
        ols = LinearRegression(fit_intercept=False)
        ols.fit(T_res, Y_res)
        theta_s = ols.coef_

        # HC1 sandwich SEs for vector DML (joint multivariate OLS); bootstrap would be expensive
        eps = Y_res - T_res @ theta_s
        se_s = np.zeros(d)
        for j in range(d):
            t_j = T_res[:, j]
            T2_j = (t_j ** 2).sum()
            if T2_j > 0:
                se_s[j] = np.sqrt(n / (n - 1) * np.sum((t_j * eps) ** 2) / T2_j ** 2)

        seed_thetas.append(theta_s)
        seed_ses.append(se_s)

    seed_thetas = np.array(seed_thetas)  # (n_seeds, d)
    seed_ses = np.array(seed_ses)        # (n_seeds, d)

    # Median (not mean) across seeds: robust to outlier partitions
    theta = np.median(seed_thetas, axis=0)
    se_within = np.median(seed_ses, axis=0)
    se_across = np.std(seed_thetas, axis=0, ddof=1)
    # Within = fold sampling error; across = partition-assignment variance. Both needed for honest inference
    se = np.sqrt(se_within ** 2 + se_across ** 2)

    # p-values (two-sided normal)
    z = theta / np.where(se > 0, se, 1e-10)
    pval = 2 * (1 - stats.norm.cdf(np.abs(z)))

    # BH within each backend, not across backends -- each backend is a separate model
    try:
        from statsmodels.stats.multitest import multipletests
        reject, p_adj, _, _ = multipletests(pval, alpha=alpha, method='fdr_bh')
    except ImportError:
        # Manual BH
        m = len(pval)
        sorted_idx = np.argsort(pval)
        p_adj = np.zeros(m)
        for rank, idx in enumerate(sorted_idx):
            p_adj[idx] = pval[idx] * m / (rank + 1)
        for i in range(m - 2, -1, -1):
            p_adj[sorted_idx[i]] = min(p_adj[sorted_idx[i]],
                                        p_adj[sorted_idx[i + 1]])
        p_adj = np.minimum(p_adj, 1.0)
        reject = p_adj < alpha

    return {
        'theta': theta,
        'se': se,
        'se_within': se_within,
        'se_across': se_across,
        'pval': pval,
        'p_bh': p_adj,
        'bh_sig': reject,
        'n_sig': reject.sum(),
        'seed_thetas': seed_thetas,
        'seed_ses': seed_ses,
        'n_seeds': n_seeds,
        'n_folds': n_folds,
        'n_fits': n_seeds * n_folds,
    }


def estimand_c(theta, emb_df, emb_cols):
    """
    Project zone embeddings onto theta to get causal scores.
    Returns DataFrame with zone_class, context, causal_score.
    """
    # s_z = e_z dot theta: positive = AT-promoting text, negative = car-promoting
    E = emb_df[emb_cols].values  # (n_zones, d)
    # Truncate theta if dimensions don't match (hybrid case)
    d = min(len(theta), E.shape[1])
    scores = E[:, :d] @ theta[:d]

    result = pd.DataFrame({
        'zone_class': emb_df['zone_class'].values,
        'context': emb_df['context'].values,
        'causal_score': scores,
    })
    return result.sort_values('causal_score', ascending=False)


