def build_hybrid_embeddings(llm_path, st_path, output_path):
    """
    Concatenate LLM-scored features with sentence-transformer PCA
    components to create a hybrid embedding.
    """
    llm_df = pd.read_csv(llm_path)
    st_df = pd.read_csv(st_path)

    llm_cols = [c for c in llm_df.columns if c.startswith('feat_')]
    st_cols = [c for c in st_df.columns if c.startswith('emb_')]

    # Merge on zone_class
    merged = llm_df[['zone_class', 'context'] + llm_cols].merge(
        st_df[['zone_class'] + st_cols], on='zone_class', how='inner')

    # Rename to unified emb_ prefix
    new_cols = {}
    idx = 0
    for c in llm_cols:
        new_cols[c] = f'emb_{idx:03d}'
        idx += 1
    for c in st_cols:
        new_cols[c] = f'emb_{idx:03d}'
        idx += 1
    merged = merged.rename(columns=new_cols)

    merged.to_csv(output_path, index=False)
    n_dims = len(llm_cols) + len(st_cols)
    print(f'Built hybrid embeddings: {len(merged)} zones × {n_dims} dims')
    print(f'  LLM dims: {len(llm_cols)}  |  ST dims: {len(st_cols)}')
    print(f'  Saved: {output_path}')
    return merged


