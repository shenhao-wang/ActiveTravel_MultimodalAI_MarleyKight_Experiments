def load_base_data():
    """Load trips, zoning, ACS — everything except embeddings."""
    trips = pd.read_csv(TRIPS_PATH)
    zon_csv = pd.read_csv(ZON_CSV_PATH).dropna(subset=['ZONECLASS'])
    zone_feat = pd.read_csv(FEAT_PATH)

    trips['active_travel'] = trips['mode_type'].isin([1, 2]).astype(int)
    area_col = ('ShapeSTAre_corrected' if 'ShapeSTAre_corrected' in zon_csv.columns
                else 'ShapeSTAre')

    # Dominant zone per BG
    dominant = (zon_csv.sort_values(area_col, ascending=False)
                .drop_duplicates(subset='GEOID', keep='first')[['GEOID', 'ZONECLASS']]
                .rename(columns={'ZONECLASS': 'dominant_zone'}))
    dominant['zone_context'] = 'Other/PD'
    dominant.loc[dominant['dominant_zone'].isin(RESIDENTIAL), 'zone_context'] = 'Residential'
    dominant.loc[dominant['dominant_zone'].isin(NON_RESIDENTIAL), 'zone_context'] = 'Non-Residential'
    dominant['residential_zone'] = dominant['dominant_zone'].isin(RESIDENTIAL).astype(int)

    # Scraper features (needed for confounder set)
    zon_m = zon_csv.merge(
        zone_feat[['zone_class', 'ped_score_norm', 'front_setback',
                   'lot_width', 'max_height']],
        left_on='ZONECLASS', right_on='zone_class', how='left')

    def weighted_mean(group, val_col):
        mask = group[val_col].notna()
        if mask.sum() == 0:
            return np.nan
        return np.average(group.loc[mask, val_col], weights=group.loc[mask, area_col])

    def entropy(group):
        total = group[area_col].sum()
        if total == 0:
            return 0.0
        p = group[area_col] / total
        p = p[p > 0]
        return float(-np.sum(p * np.log(p)))

    scraper_agg = (zon_m.groupby('GEOID')
                   .apply(lambda g: pd.Series({
                       'ped_score_weighted': weighted_mean(g, 'ped_score_norm'),
                       'front_setback_avg': weighted_mean(g, 'front_setback'),
                       'lot_width_avg': weighted_mean(g, 'lot_width'),
                       'max_height_avg': weighted_mean(g, 'max_height'),
                       'zoning_entropy': entropy(g),
                   }))
                   .reset_index())

    bg_features = dominant.merge(scraper_agg, on='GEOID', how='left')

    # ACS
    acs_cols = []
    if os.path.exists(ACS_PATH):
        acs = pd.read_csv(ACS_PATH)
        ACS_VARS = ['median_hh_income', 'pct_bachelors_plus',
                    'pct_white_nonhisp', 'pct_black_nonhisp', 'pct_hispanic',
                    'pct_poverty', 'pct_owner_occ']
        acs_cols = [c for c in ACS_VARS if c in acs.columns]
        bg_features = bg_features.merge(acs[['GEOID'] + acs_cols],
                                        on='GEOID', how='left')

    return trips, zon_csv, bg_features, acs_cols, area_col


def build_bg_embeddings(zon_csv, emb_df, emb_cols, area_col):
    """Area-weighted embedding aggregation to block-group level."""
    emb_lookup = emb_df.set_index('zone_class')[emb_cols].to_dict('index')
    bg_rows = []
    for geoid, grp in zon_csv.groupby('GEOID'):
        total = grp[area_col].sum()
        vec, cov = np.zeros(len(emb_cols)), 0.0
        for _, row in grp.iterrows():
            z = row['ZONECLASS']
            if z in emb_lookup:
                w = row[area_col] / total
                vec += np.array(list(emb_lookup[z].values())) * w
                cov += w
        if cov > 0:
            bg_rows.append({'GEOID': geoid, **dict(zip(emb_cols, vec))})
    return pd.DataFrame(bg_rows)


